package design_pattern.behavioral_patterns.mediator_pattern.dto;

import design_pattern.behavioral_patterns.mediator_pattern.ChatRoom;

public class User {
    private String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public User(String name){
        this.name  = name;
    }

    public void sendMessage(String message){
        ChatRoom.showMessage(this,message);
    }
}